package com.flipkart.bean;

/**
 * Role enum represents different roles in the Flipkart system.
 * It includes roles for administrators, gym owners, and customers.
 */
public enum Role {
    /**
     * Represents the administrator role.
     */
    ADMIN,      // Represents the administrator role
    /**
     * Represents the gym owner role.
     */
    GYMOWNER,   // Represents the gym owner role
    /**
     * Represents the customer role.
     */
    CUSTOMER    // Represents the customer role
}
